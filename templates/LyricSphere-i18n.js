let _currentLang = 'zh-CN';

function normalizeLanguageCode(raw) {
    if (!raw) return 'zh-CN';
    const s = String(raw).trim();
    if (s.startsWith('zh')) return 'zh-CN';
    if (s.startsWith('en')) return 'en';
    return 'zh-CN';
}

function readLanguage() {
    try {
        const stored = JSON.parse(localStorage.getItem(I18N_STORAGE_KEY) || '{}');
        if (stored && stored.language) return normalizeLanguageCode(stored.language);
    } catch (e) {}
    const navLang = normalizeLanguageCode(navigator.language || navigator.userLanguage || '');
    return navLang;
}

function writeLanguage(lang) {
    try {
        const prev = JSON.parse(localStorage.getItem(I18N_STORAGE_KEY) || '{}');
        prev.language = lang;
        localStorage.setItem(I18N_STORAGE_KEY, JSON.stringify(prev));
    } catch (e) {
        localStorage.setItem(I18N_STORAGE_KEY, JSON.stringify({ language: lang }));
    }
}

function t(key, params) {
    const lang = _currentLang || 'zh-CN';
    const messages = I18N_MESSAGES[lang] || I18N_MESSAGES['zh-CN'];
    let text = messages[key];
    if (text === undefined) {
        text = (I18N_MESSAGES['zh-CN'] || {})[key];
    }
    if (text === undefined) {
        console.warn('[i18n] Missing key:', key);
        return key;
    }
    if (params && typeof params === 'object') {
        Object.keys(params).forEach(k => {
            text = text.replace(new RegExp('\\{' + k + '\\}', 'g'), params[k]);
        });
    }
    return text;
}

function setLanguage(lang) {
    lang = normalizeLanguageCode(lang);
    _currentLang = lang;
    writeLanguage(lang);
    document.documentElement.lang = lang === 'zh-CN' ? 'zh' : 'en';
    applyI18nToStaticElements();
    refreshDynamicI18nTexts();
}

function applyI18nToStaticElements() {
    // data-i18n: textContent (safe for leaf nodes only)
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (!key) return;
        let params = null;
        const paramsAttr = el.getAttribute('data-i18n-params');
        if (paramsAttr) {
            try { params = JSON.parse(paramsAttr); } catch (e) {}
        }
        el.textContent = t(key, params);
    });
    // data-i18n-placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (key) el.placeholder = t(key);
    });
    // data-i18n-title
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        if (key) el.title = t(key);
    });
    // data-i18n-alt
    document.querySelectorAll('[data-i18n-alt]').forEach(el => {
        const key = el.getAttribute('data-i18n-alt');
        if (key) el.alt = t(key);
    });
    // data-i18n-html: innerHTML
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        if (key) el.innerHTML = t(key);
    });
}

// 动态文本刷新回调列表
const _dynamicI18nCallbacks = [];
function registerDynamicI18nCallback(fn) {
    _dynamicI18nCallbacks.push(fn);
}
function refreshDynamicI18nTexts() {
    _dynamicI18nCallbacks.forEach(fn => {
        try { fn(); } catch (e) { console.error('[i18n] dynamic callback error', e); }
    });
}

// 弹窗封装
function alertI18n(key, params) {
    if (typeof key === 'string' && key.startsWith('i18n:')) {
        alert(t(key.slice(5), params));
    } else {
        alert(key);
    }
}
function confirmI18n(key, params) {
    if (typeof key === 'string' && key.startsWith('i18n:')) {
        return confirm(t(key.slice(5), params));
    }
    return confirm(key);
}
function promptI18n(key, params, defaultVal) {
    if (typeof key === 'string' && key.startsWith('i18n:')) {
        return prompt(t(key.slice(5), params), defaultVal);
    }
    return prompt(key, defaultVal);
}

